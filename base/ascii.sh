        echo "$logo_debian"
    elif [[ "$DISTRO_ID" == "nixos" ]];then
        echo "$logo_nixos"
    elif [[ "$DISTRO_ID" == "ubuntu" ]];then
        echo "$logo_ubuntu"
    elif [[ "$DISTRO_ID" == "arch" ]];then
        echo "$logo_arch"
    elif [[ "$DISTRO_ID" == "fedora" ]];then
        echo "$logo_fedora"
    elif [[ "$DISTRO_ID" == "kali" ]];then
        echo "$logo_kali"
